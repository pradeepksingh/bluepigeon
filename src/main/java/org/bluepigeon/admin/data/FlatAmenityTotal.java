package org.bluepigeon.admin.data;

public class FlatAmenityTotal {

	private int amenityId;
	private Double amenityWeightage;
	private Double totalSubstageWeightage;
	public int getAmenityId() {
		return amenityId;
	}
	public void setAmenityId(int amenityId) {
		this.amenityId = amenityId;
	}
	public Double getAmenityWeightage() {
		return amenityWeightage;
	}
	public void setAmenityWeightage(Double amenityWeightage) {
		this.amenityWeightage = amenityWeightage;
	}
	public Double getTotalSubstageWeightage() {
		return totalSubstageWeightage;
	}
	public void setTotalSubstageWeightage(Double totalSubstageWeightage) {
		this.totalSubstageWeightage = totalSubstageWeightage;
	}
	
	
}
